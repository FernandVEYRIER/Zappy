using UnityEngine;
using System.Collections;

public class MapBlock {

        public int Food;
        public int Lenemate;
        public int Deraumere;
        public int Sibur;
        public int Mendiane;
        public int Phiras;
        public int Thystame;

        // Set all properties of a cubeMap
        public MapBlock(int food, int lenemate, int deraumere, int sibur, int mendiane, int phiras, int thystame)
        {
            Food = food;
            Lenemate = lenemate;
            Deraumere = deraumere;
            Sibur = sibur;
            Mendiane = mendiane;
            Phiras = phiras;
            Thystame = thystame;
        }
}
